                <p>Before changes to GCSE first taken in 2017, the IGCSE was often considered to be more similar to the older O-Levels qualification than the current GCSE in England, and for this reason was often argued to be a more rigorous and more difficult examination.[9] Before the early 2010s, most schools offering the IGCSE were private international schools for expatriate children around the world. However in the 2010s, an increasing number of independent schools within the United Kingdom also began offering IGCSEs as an alternative to conventional British GCSEs for international IGCSE subjects, on the supposed basis that it is more challenging than the national curriculum.[10] A comparison between GCSEs and IGCSEs was conducted by the Department of Education in 2019. The study found that it was easier to achieve a grade A in English Language and English Literature in IGCSEs but harder to achieve a grade A in science subjects. Most other subjects were roughly equivalent.[11]</p>
