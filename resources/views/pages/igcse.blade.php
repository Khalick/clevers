                <h4>United Kingdom</h4>
                <p>The official status of IGCSEs has changed several times in the UK.

                    In 2013 the Office of Qualifications and Examinations Regulation (Ofqual) allowed more use of IGCSE subjects in state-funded schools. Ofqual allowed the use of Cambridge IGCSE exams under the name of "Cambridge International Certificates".[19] Initially, 16 Cambridge IGCSE syllabuses received UK government accreditation. Following that, the UK government announced that the 16 accredited Cambridge IGCSE syllabuses could also be funded in state-maintained schools. Subsequently, Cambridge IGCSE German and Spanish were also accredited and funded, taking the total number of accredited and funded Cambridge IGCSEs to 18. For accreditation purposes, the syllabuses are referenced as "Cambridge International Certificates" in the UK, although they are known across the world as Cambridge IGCSEs. The IGCSE is offered by two examination boards in the UK, one being Edexcel, and the other one being AQA.[20]

                    However, from 2017 the government decided to exclude IGCSEs from official performance tables, and consequentially entries from state schools have fallen.[21] So that whilst "international GCSEs no longer meet the condition of funding; however, they do continue to count as equivalent to GCSEs for the purposes of recognising prior attainment."[22]

